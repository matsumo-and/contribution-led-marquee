/**
 * Add shadow to the right of lit pixels
 * - Adds 0.5 shadow wherever a lit pixel (1) has empty space (0) to its right within columns 0-4
 * - Adds 6th column shadow if rightmost pixel (column 4) is lit
 * Makes 5x7 characters into 6x7
 */
function addShadow(bitmap: number[][]): number[][] {
  const result: number[][] = [];

  for (let y = 0; y < bitmap.length; y++) {
    const row: number[] = [...bitmap[y]];

    // Add shadow within the character (columns 0-4)
    // If a pixel is lit (1) and the next pixel is empty (0), add shadow
    for (let x = 0; x < 4; x++) {  // Check positions 0-3
      if (row[x] === 1 && row[x + 1] === 0) {
        row[x + 1] = 0.5;  // Add shadow
      }
    }

    // Add shadow in 6th column if rightmost pixel is lit
    const hasRightPixel = row[4] === 1;
    if (hasRightPixel) {
      row.push(0.5);  // Shadow
    } else {
      row.push(0);    // No shadow
    }

    result.push(row);
  }

  return result;
}

/**
 * Get bitmap for a character with shadow
 */
export function getCharBitmap(char: string): number[][] | null {
  const baseBitmap = FONT_DATA[char];
  if (!baseBitmap) {
    return null;
  }

  return addShadow(baseBitmap);
}

/**
 * Get character width (including shadow)
 */
export function getCharWidth(char: string): number {
  const bitmap = getCharBitmap(char);
  if (!bitmap || bitmap.length === 0) {
    return 0;
  }
  return bitmap[0].length;
}

